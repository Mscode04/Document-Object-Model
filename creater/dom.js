// `element.removeChild()`**: Removes a child element from an existing element.
// `element.replaceChild()`**: Replaces an existing child element with a new element.


// `element.setAttribute()`**: Sets the value of an attribute on an element.
// `element.getAttribute()`**: Gets the value of an attribute on an element.
// `element.removeAttribute()`**: Removes an attribute from an element.


// `element.classList.add()`**: Adds one or more class names to an element.
// `element.classList.contains()`**: Checks if an element has a specific class name.
// `element.classList.toggle()`**: Toggles a class name on an element.
// `element.classList.remove()`**: Removes one or more class names from an element.



// `element.removeEventListener()`**: Removes an event listener from an element.
// `element.addEventListener()`**: Adds an event listener to an element.
  

// `element.previousSibling`**: Gets the previous sibling of an element.
// `element.nextSibling`**: Gets the next sibling of an element.
// `element.lastChild`**: Gets the last child of an element.
// `element.firstChild`**: Gets the first child of an element.
// `element.childNodes`**: Gets a list of child nodes of an element.
// `element.parentNode`**: Gets the parent node of an element.


// `element.textContent`**: Sets or gets the text content of an element.
// `element.innerHTML`**: Sets or gets the HTML content inside an element.
